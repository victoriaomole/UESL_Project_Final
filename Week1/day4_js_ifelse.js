// 🧠 Day 4 – JavaScript Conditions (if / else)

let score = 85;

if (score >= 90) {
    console.log("🏆 Excellent performance!");
} else if (score >= 75) {
    console.log("🎯 Good job, keep improving!");
} else {
    console.log("📚 Needs more effort.");
}

// Practical Example
let isLoggedIn = true;
if (isLoggedIn) {
    console.log("✅ Welcome back, Victoria!");
} else {
    console.log("🚪 Please log in to continue.");
}

